/**
* Função para imprimir o Array
*/

if (!function_exists('pre')) {
  /**
  * Description
  * @param type $array
  * @return type
  */
  function pre($array) {
      echo '<pre>';
      print_r($array);
      echo '</pre>';
  }
}

